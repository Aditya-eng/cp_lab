# Importing functions from external packages

import math
import random

# Example 1: Using math package
print("Math package examples:")
print("Square root of 16:", math.sqrt(16))
print("Ceiling of 3.4:", math.ceil(3.4))
print("Factorial of 5:", math.factorial(5))
print("Value of pi:", math.pi)

# Example 2: Using random package
print("\nRandom package examples:")
print("Random integer between 1 and 10:", random.randint(1, 10))
print("Random choice from a list:", random.choice(["apple", "banana", "cherry", "date"]))
print("Random float between 0 and 1:", random.random())
